            <div class="i18n_a4_intro"><a href=\"https://elastic.co/\" target=\"_blank\">Elastic</a> is a set of free softwares for data acquisition, storage and analysis. I share with you my observations on this very complete tool. I have summarized in a infography how it works and the pro and cons of its use.</div>
            <br><br>
            <div class="i18n_a4_content"><div> <h3> Infography </h3> <div style=\"text-align: center;\"> <img src=\"./_include/img/nz/infographie2_en.png\" alt=\"ElasticSuite infography\" class=\"myPictures myPicture-large\"></div></div> <div> <h3> Description</h3> <p> The Elastic suite is so called because it is composed of several softwares that are used one after the other and are adaptable to all types of use cases. Its objective is to collect, store and analyze all kinds of data. This can be done locally or in the cloud. These open source software bricks are available to the public, free of charge, <a href=\"https://www.elastic.co/downloads\" target=\"_blank\">here</a>. </p> <h4>Collect and transform</h4>    <p><strong><a href=\"https://www.elastic.co/products/beats\" target=\"_blank\">BEATS</a></strong> software versions (metricbeats, filebeats, auditbeats, etc.) allow you to retrieve the different types of data. </p>               <p>They send them to <strong><a href=\"https://www.elastic.co/products/logstash\" target=\"_blank\">LOGSTASH</a></strong>, which transforms the data using various filters. For example, convert IP addresses to GPS coordinates, or transform a line of a CSV file into a data file with its parameters (I will detail the structure of the classic database in another article).</p>   <h4>Store and analyze</h4>   <p>The data are then sent to <strong><a href=\"https://www.elastic.co/products/elasticsearch\" target=\"_blank\">ELASTICSEARCH</a></strong>, which is the heart of the Elastic suite. It stores the data and lets you ask precise queries, formulated in the language of your choice (Curl, Java, C#, Python, JavaScript, PHP, Perl, Ruby,...) facilitating the interaction. </p> <h4>Visualize and manage</h4> <p>Finally, the data can be displayed on <strong><a href=\"https://www.elastic.co/products/kibana\" target=\"_blank\">KIBANA</a></strong>, which is an interface for data manipulation. Graphs, maps, etc. can be quickly formatted to display and use the data. </p> <h3>Conclusion</h3> <p>A promising tool! I will present you in a next article how I use it and other examples of use.</p> <p>See you soon!</p></div></div>
